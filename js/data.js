// Функция генерации данных популярности по годам
function generatePopularity(startYear, endYear, peakYears) {
    const data = [];
    
    for (let year = startYear; year <= endYear; year += 10) {
        let popularity = 20 + Math.random() * 10; // Базовая популярность
        
        // Добавляем пики
        peakYears.forEach(peakYear => {
            const distance = Math.abs(year - peakYear);
            if (distance < 100) {
                popularity += (100 - distance) * 0.5;
            }
        });
        
        data.push({
            year: year,
            value: Math.min(100, popularity)
        });
    }
    
    return data;
}

